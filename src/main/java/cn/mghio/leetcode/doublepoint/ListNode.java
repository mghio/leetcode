package cn.mghio.leetcode.doublepoint;

/**
 * @author mghio
 * @since 2022-02-27
 */
public class ListNode {

  int val;
  ListNode next;

  public ListNode() {
  }

  public ListNode(int val) {
    this.val = val;
  }

  public ListNode(int val, ListNode next) {
    this.val = val;
    this.next = next;
  }
}
